            <br>
            <p>The overall scope of this project is to design and implement an irrigated garden for the Mkutani Primary school, which serves around 600 students. There is currently no vegetable garden on the school property. The headmaster identified that while most students go home for lunch, around a third of the students live too far away and rely on meals provided by the school. The aim of the project is to provide supplemental food  at mealtimes for these students. The fruits and vegetables the headmaster hopes to plant are spinach, local spinach, Chinese cabbage, pawpaw, and guava. The garden will also serve as an educational tool for students and adults in the school community to learn agricultural skills. </p>
            <p>Our project has completed the assessment and alternatives analysis phases. During our assessment trip in June 2022, the team collected geospatial data of the garden site, tested the water quality data of potential water sources, tested soil quality and drainage data, and interviewed parents and farmers about local farming practices and community preferences for the garden. Using the information collected during the assessment phase, we recently completed the alternatives analysis phase of our project, where we investigated several different water sources and irrigation methods for the garden. Based on our analysis, the best water source is a river about 1 kilometer away from the school. We are planning to drill an infiltration well near the riverbank, which will allow us to access water in the river's water table without pulling directly from the surface water, which is turbid and contaminated with E. Coli. For irrigation, we plan to construct a gravity-fed perforated pipe irrigation system. Our team is currently working on the design for both the water delivery and irrigation systems. This includes making CAD diagrams of system components, doing calculations to determine pump size, working with experts on irrigation, and more.</p>
